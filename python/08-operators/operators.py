def main():
    x, y = 15, 5
    print("{} + {} = {}".format(x, y, x + y))
    print("{} - {} = {}".format(x, y, x - y))
    print("{} * {} = {}".format(x, y, x * y))
    print("{} / {} = {}".format(x, y, x / y))
    print("{} // {} = {}".format(x, y, x // y))
    print("{} % {} = {}".format(x, y, x % y))

if __name__ == "__main__": main()
